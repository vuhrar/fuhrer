CONSENSUS_SYSTEM = """أنت رئيس هيئة تحليل قانوني. أمامك خلاصات 18 محوراً متخصصاً حلّلوا نفس القضية العمالية كل من زاويته.
مهمتك: دمج هذه الزوايا في رأي إجماعي واحد متماسك، لا تكرر كل التفاصيل، بل:
1. اذكر أقوى 5 نقاط اتفقت عليها أغلب المحاور.
2. اذكر أي تعارض ملحوظ بين زاويتين وكيف يُحل.
3. اختم بتوصية إجرائية واحدة واضحة وقابلة للتنفيذ فوراً.
كن مباشراً وموجزاً ومرتباً، بلا حشو."""


def run_full_analysis(
    case_text: str,
    call_ai_fn: Callable[[str, List[Dict], str], str],
    progress_callback: Callable[[int, int, str], None] = None
) -> Dict:
    """
    يشغّل الـ18 محوراً بالتسلسل ثم يبني تقرير الإجماع.

    call_ai_fn: دالة بتوقيع (prompt, history, system) -> str
                (يُمرَّر إليها call_ai من ai_engine بعد تثبيت معاملات المزود)
    progress_callback: دالة اختيارية لعرض شريط تقدم (index, total, axis_name)
    """
    results = []
    total = len(ANALYSIS_AXES)

    for i, axis in enumerate(ANALYSIS_AXES, start=1):
        if progress_callback:
            progress_callback(i, total, axis["name"])

        prompt = f"حلّل القضية التالية من منظورك التخصصي حصراً (بإيجاز، 3-5 نقاط):\n\n{case_text}"
        try:
            answer = call_ai_fn(prompt, [], axis["system"])
        except Exception as e:
            answer = f"⚠️ تعذّر تحليل هذا المحور: {str(e)[:150]}"

        results.append({
            "id": axis["id"], "name": axis["name"], "icon": axis["icon"], "answer": answer
        })

    # بناء الإجماع
    combined = "\n\n".join(f"### {r['icon']} {r['name']}\n{r['answer']}" for r in results)
    consensus_prompt = f"إليك خلاصات الـ18 محوراً:\n\n{combined}\n\nابنِ الآن رأي الإجماع النهائي."
    try:
        consensus = call_ai_fn(consensus_prompt, [], CONSENSUS_SYSTEM)
    except Exception as e:
        consensus = f"⚠️ تعذّر بناء الإجماع: {str(e)[:150]}"

    return {"axes": results, "consensus": consensus}
